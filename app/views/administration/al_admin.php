<h5 class="col s12 accent-3 margin-top-small radius-top align-center-small">Les établissements à l'université de Douala</h5>
		<div class="content-bloc">
			<div class="col 12 introduction-bloc">
				<P>
					Avec environ trois cent (300) filières, dont près d'une centaine professionnalisantes, l'Université de Douala se positionne comme leader en termes de professionnalisation des enseignements au Cameroun.
				</P>
				<p>
					L’année académique à l’Université de Douala est composée de deux (02) semestres de quatorze (14) semaines chacun. Elle s’étend d’octobre à juillet, avec une session de rattrapage en septembre, toutes disciplines et tous cycles LMD. Les langues de travail sont le français et l’anglais. Toutefois, l’étudiant est autorisé à composer dans la langue de son choix (français ou anglais) ; de même, l’enseignant dispense ses cours dans la langue de son choix (français ou anglais).
				</p>
				<p>
					En plus de ses 11 Etablissements à vocation classique (FLSH, FSJP, FSEGA, FGI, ESSEC, ENSET, IUT, IBA, ISH, FMSP et FS), l’Université de Douala dispose en son sein de deux structures spécialisées :
				</p>
