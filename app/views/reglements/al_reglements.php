<h5 class="col s12 accent-3 margin-top-small radius-top align-center-small">Le règlement interieur du CIUD</h5>
<div class="col 12 content-bloc">

	 		<h5 class="col s12 list-number">Disposition générale</h5>
    		<h6 class="col s12 sub-list-number">1. Nom</h6>
	     	<div class="col 12 content-list-number">
	        	<p>
	        		L’acronyme CIUD (Club informatique de l'Université de Douala) et le mot club ou association sont aussi utilisés dans ces règlements généraux pour désigner l'organisme.
	        	</p>
	     	</div>
    		<!--  -->
    		<h6 class="col s12 sub-list-number">2. Siège social</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le siège social du club est situé à l’adresse déterminée par les membres du bureau executif.
				</p>
	     	</div>
	     	<!--  -->
    		<h6 class="col s12 sub-list-number">3. Nature</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le club est un organisme à but non lucratif qui regroupe des personnes intéressées à partager leurs connaissances et à s’entraider en informatique. Le club a un rôle d’animation et de service. Il veille à ce que toute activité soit conforme à sa mission et sa politique.
				</p>
				<p>
					Le partage de connaissances se fait sans aucune forme de garantie. Le club et ses vallants dynamiques ne garantissent pas l’exactitude de l’information présentée et ne sont pas responsables des conséquences de l’utilisation de cette information par les membres ou par toute autre personne.
				</p>
	     	</div> 
	     	<!--  -->
    		<h6 class="col s12 sub-list-number">4. Objets</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Les objets pour lesquels l’association est constituée sont les suivants :
				</p>
				 <div class="col s12 list-elmt-padding">
		        	<STRONG><span class="ciud-icons">&#203;</span>Diffuser de l’information sur l’utilisation des micro-ordinateurs;</STRONG>
		     	 </div>

	        	 <div class="col s12 list-elmt-padding">
		        	<STRONG><span class="ciud-icons">&#203;</span>Favoriser un service d’entraide pour les membres;</STRONG>
		     	 </div>
	     	</div> 
	     	<!--  -->
	     	<h6 class="col s12 sub-list-number">5. Membres</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Est membre régulier du CIUD toute personne qui s’inscrit. L’état de membre du club se termine suivant la période pour laquelle le membre a effectué son inscription. Ils ont le droit de participer à toutes les activités du club.
				</p>
				<p>
					Peut être reconnue comme membre associé toute personne qui est désignée comme tel en vertu de l’engagement social du club. Cette personne a le droit de participer à toutes les activités du club.
				</p>
				<p>
					Tout membre peut être exclu du club par le bureau executif :
				</p>
				<p>
					<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Pour infraction grave à un des règlements de l’association; ou</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>pour conduite ou activités jugées nuisibles à l’association; ou</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>pour détournement ou appropriation d’un actif de l’association.</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Cette exclusion prend effet à la réception par le membre d’un avis écrit et envoyé par courrier recommandé ou par SMS ou toute autre forme de communication.</STRONG>
		     	 	</div>
				</p>
	     	</div> 
	     	<!--  -->
	     	<h6 class="col s12 sub-list-number">6. Assemblée générale</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Tous les membres réguliers peuvent participer à une assemblée générale.
				</p>
	     	</div>
	     		<!--  -->
	     		<!--  -->
	     	<h5 class="col s12 list-number">Bureau executif</h5>
	     	<h6 class="col s12 sub-list-number">1. Rôle</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le Bureau executif oriente la gestion générale du club et s’assure de sa bonne qualité.
				</p>
	     	</div>
	     		<!--  -->
	     	<h6 class="col s12 sub-list-number">2. Composition</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le Bureau executif se compose d’un maximum de dix (10) membres réguliers élus pour un ans lors d’une assemblée générale annuelle. Ces membres sont élus chaque année.
				</p>
	     	</div>
	     		<!--  -->
	     	<h6 class="col s12 sub-list-number">3. Élections des membres du bureau executif</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le Bureau executif est voté chaque année par les etudiants de l'université de douala lors d'une assemblée génerale organisée par le rectorat.
				</p>
	     	</div>
	     		<!--  -->
	     	<h6 class="col s12 sub-list-number">4. Bureau élargi</h6>
	     	<div class="col 12 content-list-number">
				<p>
					Le Bureau executif peut déléguer certains pouvoirs aux membres reguliers. Fonctions :
				</p>
				<P>
					<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Proposer une mission, une politique et des orientations;</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Assurer que la mission, la politique et les orientations du club sont respectées;</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Etudier les dossiers présentés par les membres du conseil d’administration ou les membres du club;</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Décider des questions ordinaires pour la bonne marche du club;</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Déterminer les tâches à réaliser et de les répartir entre ses membres;</STRONG>
		     	 	</div>
		     	 	<div class="col s12 list-elmt-padding">
		        		<STRONG><span class="ciud-icons">&#203;</span>Décider des questions ordinaires pour la bonne marche du club;</STRONG>
		     	 	</div>
				</P>
	     	</div>
